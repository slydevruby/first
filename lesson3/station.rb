require_relative 'instance_counter'

class Station
  @@stations = []
  attr_reader :name, :trains

  include InstanceCounter

  def self.all
    @@stations
  end

  def initialize(name)
    @name = name
    @trains = []
    @@stations << self
    register_instance
  end

  def accept_train(train)
    @trains << train
  end

  def send_train(train)
    @trains.delete(train)
  end

  def get_trains_by_type(type)
    @trains.select { |train| train.type == type }.size
  end
end
